        this.txtBuscar.setText("");
        this.txtDescripcion.setText("");
        this.txtLargo.setText("");
        DefaultComboBoxModel modeloCombo;
        modeloCombo = new DefaultComboBoxModel(this.controlador.listarTipoArticulo().toArray());
